package eje1_POO;

public class Direccion {
    
    public String calle;
    public String carrera;
    public int numero;
    public boolean sobre_carrera;

    public Direccion(String calle,String carrera,int numero,boolean sobre_carrera){
        this.calle = calle;
        this.carrera = carrera;
        this.numero = numero;
        this.sobre_carrera = sobre_carrera;
    }

}
